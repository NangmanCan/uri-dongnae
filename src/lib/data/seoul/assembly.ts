// 선거구명으로 국회의원 찾기
export function findAssemblyMember(district: string): Member | undefined {
  return seoulAssembly.find(m => m.district === district);
}

// 구 이름으로 해당 선거구의 국회의원들 찾기 (갑/을/병 포함)
export function findAssemblyMembersByGu(guName: string): Member[] {
  const searchTerm = guName.replace('구', '');
  return seoulAssembly.filter(m => m.district.includes(searchTerm));
}
