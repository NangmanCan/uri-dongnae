/**
 * 구 이름으로 해당 구의 서울시의원 찾기
 */
export function getCouncilMembersByGu(guName: string): Member[] {
  const normalized = guName.replace('구', '');
  return seoulCouncilMembers.filter(m => m.district.includes(normalized));
}

/**
 * 선거구로 서울시의원 찾기
 */
export function getCouncilMemberByDistrict(district: string): Member | undefined {
  return seoulCouncilMembers.find(m => m.district === district);
}
